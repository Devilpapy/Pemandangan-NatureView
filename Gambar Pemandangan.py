pen.pendown()
pen.color('white')
pen.begin_fill()
pen.circle(30)
pen.end_fill()

pen.penup()
pen.goto(-70, 220)
pen.pendown()
pen.begin_fill()
pen.circle(30)
pen.end_fill()

pen.penup()
pen.goto(-30, 220)
pen.pendown()
pen.begin_fill()
pen.circle(30)
pen.end_fill()

pen.penup()
pen.goto(10, 200)
pen.pendown()
pen.begin_fill()
pen.circle(30)
pen.end_fill()

pen.penup()
pen.goto(40, 180)
pen.pendown()
pen.begin_fill()
pen.circle(30)
pen.end_fill()

t.color('#808080')
t.begin_fill()
